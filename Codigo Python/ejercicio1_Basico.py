'''I. Crear un programa en Phyton que pida por pantalla nombre, primer apellido, 
segundo apellido, edad, código postal, población y teléfono. Posteriormente mostrará los resultados por terminal.'''

nombre = input("Ingresa el nombre: ")
primerApellido = input("Ingresa el primer apellido: ")
segundoApellido = input("Ingresa el segundo apellido: ")
edad = input("Ingresa la edad: ")
codigoPostal = input("Ingresa el codigo postal: ")
poblacion = input("Ingresa la población: ")
telefono = input("Ingresa el numero de telefono: ")

print("\n******************************************************"+"\nLos datos introducidos son : "+"\nNombre: "+nombre+"\nPrimer Apellido: "+ primerApellido +
      "\nSegundo apellido: "+segundoApellido+"\nEdad: "+edad + "\nCodigo Postal: "+codigoPostal+
      "\nPoblación: "+poblacion+"\nTeléfono: "+telefono)